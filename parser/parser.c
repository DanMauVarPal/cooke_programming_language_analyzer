void error();

/* P
 * Parses strings in the language generated by the rule:
 * P ::= S
 */
void P()
{
    printf("Enter <prog>\n");

    /* Parse the statement */
    S();

    if (nextToken != EOF)
        error();

    printf("Exit <prog>\n");
} /* End of function P */

/* S
 * Parses strings in the language generated by the rule:
 * S ::= V := E Slr | cin >> V Slr | cout << E Slr | ++V Slr | --V Slr | for V in E .. E : S Slr | if C : S M Spd Slr
 */
void S()
{
    printf("Enter <stmt>\n");

    switch (nextToken) {
        // S ::= V := E Slr
        case IDENT:
            lex();
            if (nextToken != ASSIGNMENT_OP)
                error();

            lex();
            E();

            break;

        // S ::= cin >> V Slr
        case KEY_CIN:
            lex();
            if (nextToken != SHIFT_R_OP)
                error();

            lex();
            if (nextToken != IDENT)
                error();

            lex();

            break;

        // S ::= cout << E Slr
        case KEY_COUT:
            lex();
            if (nextToken != SHIFT_L_OP)
                error();

            lex();
            E();

            break;

        // S ::= ++V Slr | --V Slr
        case INC_OP:
        case DEC_OP:
            lex();
            if (nextToken != IDENT)
                error();

            lex();
            break;

        // S ::= for V in E .. E : S Slr
        case KEY_FOR:
            lex();
            if (nextToken != IDENT)
                error();

            lex();
            if (nextToken != KEY_IN)
                error();

            lex();
            E();

            if (nextToken != RANGE_OP)
                error();

            lex();
            E();

            if (nextToken != COLON)
                error();

            lex();
            S();

            printf("Exit for\n");

            break;
        
        // S ::= if C : S M Spd Slr
        case KEY_IF:
            lex();
            C();

            if (nextToken != COLON)
                error();

            lex();
            S();

            M();

            Spd();

            break;
        
        default:
            error();
    }

    Slr();

    printf("Exit <stmt>\n");
} /* End of function S */

/* Spd (else)
 * Parses strings in the language generated by the rule:
 * Spd ::= else : S | ε
 */
void Spd()
{
    printf("Enter <else>\n");

    if (nextToken == KEY_ELSE) {
        lex();
        if (nextToken != COLON)
            error();

        lex();
        S();
    }

    printf("Exit <else>\n");
} /* End of function Spd */

/* Slr
 * Parses strings in the language generated by the rule:
 * Slr ::= ; S Slr | ε
 */
void Slr()
{
    printf("Enter <s;s>\n");

    if (nextToken == SEMICOLON) {
        lex();
        if (nextToken == EOF)
            error();

        S();
    }

    printf("Exit <s;s>\n");
} /* End of function Slr */

/* M (Elif)
 * Parses strings in the language generated by the rule:
 * M ::= elif C : S M | ε
 * */
void M()
{
    printf("Enter <elif>\n");

    /* As long as the next token is "elif", get
    the next token, parse the next "or" and check : */
    if (nextToken == KEY_ELIF) {
        /* Get the next token */
        lex();
        /* Parse the first "or" */
        C();

        /* As long as the next token is :, get
        the next token, parse the next statement and enter "elif" */
        if (nextToken != COLON)
            error();
        /* Get the next token */
        lex();
        /* Parse the next statement */
        S();

        /* Enter elif function */
        M();
    }

    printf("Exit <elif>\n");
} /* End of function M */

/* C (Or)
 * Parses strings in the language generated by the rule:
 * C ::= A Clr
 * */
void C()
{
    printf("Enter <or_comp>\n");

    /* Parse the first "and" */
    A();

    /* Enter helper function */
    Clr();

    printf("Exit <or_comp>\n");
} /* End of function C */

/* Clr (Or Left-Recursion Helper)
 * Parses strings in the language generated by the rule:
 * Clr ::= or A Clr | ε
 * */
void Clr()
{
    printf("Enter <or_comp_lr_helper>\n");

    /* As long as the next token is "or", get
    the next token, parse the next relation and enter "or" helper */
    if (nextToken == BOOLEAN_OR) {
        /* Get the next token */
        lex();
        /* Parse the next and */
        A();

        /* Enter helper function */
        Clr();
    }

    printf("Exit <or_comp_lr_helper>\n");
} /* End of function Clr */

/* A (And)
 * Parses strings in the language generated by the rule:
 * A ::= R Alr
 * */
void A()
{
    printf("Enter <and_comp>\n");

    /* Parse the first relation */
    R();

    /* Enter helper function */
    Alr();

    printf("Exit <and_comp>\n");
} /* End of function A */

/* Alr (And Left-Recursion Helper)
 * Parses strings in the language generated by the rule:
 * Alr ::= and R Alr | ε
 * */
void Alr()
{
    printf("Enter <and_comp_lr_helper>\n");

    /* As long as the next token is "and", get
    the next token, parse the next relation and enter "and" helper */
    if (nextToken == BOOLEAN_AND) {
        /* Get the next token */
        lex();
        /* Parse the next relation */
        R();

        /* Enter helper function */
        Alr();
    }

    printf("Exit <and_comp_lr_helper>\n");
} /* End of function Alr */

/* R (Relation)
 * Parses strings in the language generated by the rule:
 * R ::= not R | E Rpd
 * */
void R()
{
    printf("Enter <relation_not>\n");

    /* As long as the next token is "not", get
    the next token and parse the next relation */
    if (nextToken == BOOLEAN_NOT) {
        /* Get the next token */
        lex();
        /* Parse the next relation */
        R();
    }
    /* Parse the expression and enter relation helper */
    else {
        /* Parse the expression */
        E();

        /* Enter helper function */
        Rpd();
    }

    printf("Exit <relation_not>\n");
} /* End of function R */

/* Rpd (Relation Pairwise-Disjointness Helper)
 * Parses strings in the language generated by the rule:
 * Rpd ::= < E | > E | == E | <> E | <= E | >= E
 * */
void Rpd()
{
    printf("Enter <relation_<_>_==_<>_<=_>=>\n");

    /* As long as the next token is <, >, ==, <>, <= or >=, get
    the next token and parse the next expression */
    if (nextToken == LESSER_OP
        || nextToken == GREATER_OP
        || nextToken == EQUAL_OP
        || nextToken == NEQUAL_OP
        || nextToken == LEQUAL_OP
        || nextToken == GEQUAL_OP) {
        /* Get the next token */
        lex();
        /* Parse the next expression */
        E();
    }
    // Invalid Relation Pairwise-Disjointness Helper
    else {
        error();
    }

    printf("Exit <relation_<_>_==_<>_<=_>=>\n");
} /* End of function Rpd */

/* E (Expression)
 * Parses strings in the language generated by the rule:
 * E ::= T Elr
 * */
void E()
{
    printf("Enter <expr>\n");

    /* Parse the first term */
    T();

    /* Enter helper function */
    Elr();

    printf("Exit <expr>\n");
} /* End of function E */

/* Elr (Expression Left-Recursion Helper)
 * Parses strings in the language generated by the rule:
 * Elr ::= + T Elr | - T Elr | ε
 * */
void Elr()
{
    printf("Enter <elr_+_->\n");

    /* As long as the next token is + or -, get
    the next token, parse the next term and enter the expression helper */
    if (nextToken == ADD_OP || nextToken == SUB_OP) {
        /* Get the next token */
        lex();
        /* Parse the next term */
        T();

        /* Enter helper function */
        Elr();
    }

    printf("Exit <elr_+_->\n");
} /* End of function Elr */

/* T (Term)
 * Parses strings in the language generated by the rule:
 * T ::= F Tlr
 * */
void T()
{
    printf("Enter <term>\n");

    /* Parse the first factor */
    F();

    /* Enter helper function */
    Tlr();

    printf("Exit <term>\n");
} /* End of function T */

/* Tlr (Term Left-Recursion Helper)
 * Parses strings in the language generated by the rule:
 * Tlr ::= * F Tlr | / F Tlr | % F Tlr | ε
 * */
void Tlr()
{
    printf("Enter <tlr_*_/_mod>\n");

    /* As long as the next token is *, / or %, get
    the next token, parse the next factor and enter term helper */
    if (nextToken == MULT_OP
        || nextToken == DIV_OP
        || nextToken == MOD_OP) {
        /* Get the next token */
        lex();
        /* Parse the next factor */
        F();

        /* Enter helper function */
        Tlr();
    }

    printf("Exit <tlr_*_/_mod>\n");
} /* End of function Elr */

/* F (Factor)
 * Parses strings in the language generated by the rule:
 * F ::= (E) | I | L | V | ++V | --V
 * */
void F()
{
    printf("Enter <factor>\n");

    /* As long as the next token is (, int, float, ident, inc_ident or dec_ident, get the
    next token and parse the factor type */
    switch (nextToken) {
        // F ::= (E)
        case LEFT_PAREN:
            /* Get the next token */
            lex();
            /* Parse the expression */
            E();

            /* As long as the next token is ), get
            the next token */
            if (nextToken != RIGHT_PAREN)
                error();
            
            lex();

            break;
        
        // F ::= I | L | V
        case INT_LITERAL:
        case FLOAT_LITERAL:
        case IDENT:
            lex();
            break;

        // F ::= ++V
        case INC_OP:
        // F ::= --V
        case DEC_OP:
            lex();
            if (nextToken != IDENT)
                error();

            lex();
            break;

        // Invalid factor RHS
        default:
            error();
    }

    printf("Exit <factor>\n");
} /* End of function F */

